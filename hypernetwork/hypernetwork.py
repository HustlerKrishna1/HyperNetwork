class PerTypeHeads(nn.Module):
    """
    Maintains one SVDWeightHead per matrix weight type.

    Bias vectors are stored as direct parameters (tiny — negligible cost).

    Parameter budget example (hidden=256, ffn=1024, rank=32, backbone=512):
      q_weight head  : 512×32 + 256×32 + 256×32 =  32 768 params
      ffn1_weight    : 512×32 + 1024×32 + 256×32 = 57 344 params
      ffn2_weight    : 512×32 + 256×32 + 1024×32 = 57 344 params
      6 heads total  : ≈ 230 000 params           = ~0.88 MB FP32
      All biases (6 layers × 6 types) ≈ 18 000 params
    """

    def __init__(
        self,
        backbone_dim: int,
        target_cfg:   TargetModelConfig,
        hypernet_cfg: HypernetworkConfig,
    ):
        super().__init__()
        r     = hypernet_cfg.rank
        self.rank        = r
        self.weight_keys = target_cfg.all_weight_keys
        self.matrix_keys = target_cfg.matrix_weight_keys
        self.bias_keys   = target_cfg.bias_weight_keys
        self.shapes      = target_cfg.weight_shapes
        scale            = hypernet_cfg.init_scale
        n_layers         = target_cfg.n_layers

        # ── SVD heads for matrix weights (one per weight type) ────────────────
        self.svd_heads: nn.ModuleDict = nn.ModuleDict()
        for key in self.matrix_keys:
            out_dim, in_dim = self.shapes[key]
            self.svd_heads[key] = SVDWeightHead(backbone_dim, out_dim, in_dim, r, scale)

        # ── Bias vectors stored directly (not generated) — tiny cost ─────────
        # Shape: [n_layers, bias_dim] per bias key
        self.bias_params: nn.ParameterDict = nn.ParameterDict()
        if hypernet_cfg.generate_biases:
            for key in self.bias_keys:
                (b_dim,) = self.shapes[key]
                # One bias per layer
                self.bias_params[key] = nn.Parameter(
                    torch.zeros(n_layers, b_dim)
                )
        self.generate_biases = hypernet_cfg.generate_biases
        self.n_layers        = n_layers

    def forward(
        self,
        feat:            torch.Tensor,    # [B, backbone_dim]
        weight_type_idx: torch.Tensor,    # [B]  LongTensor
        layer_idx:       torch.Tensor,    # [B]  LongTensor  (for bias lookup)
    ) -> Tuple[torch.Tensor, Optional[torch.Tensor]]:
        """
        Returns (W, b):
            W : [B, out, in]  for matrix keys
            b : [B, out]       for the matching bias (or None)
        """
        unique = weight_type_idx.unique()
        if unique.numel() == 1:
            return self._single_type_forward(feat, weight_type_idx[0].item(), layer_idx)

        # Heterogeneous batch
        B      = feat.size(0)
        device = feat.device
        results: Dict[int, Tuple] = {}
        for t in unique.tolist():
            mask  = (weight_type_idx == t)
            w, b  = self._single_type_forward(feat[mask], int(t), layer_idx[mask])
            results[int(t)] = (mask, w, b)

        first_t    = int(unique[0].item())
        _, w0, b0  = results[first_t]
        W_out = torch.zeros(B, *w0.shape[1:], device=device, dtype=feat.dtype)
        b_out = (torch.zeros(B, *b0.shape[1:], device=device, dtype=feat.dtype)
                 if b0 is not None else None)
        for t, (mask, w, b) in results.items():
            W_out[mask] = w
            if b_out is not None and b is not None:
                b_out[mask] = b
        return W_out, b_out

    def _single_type_forward(
        self,
        feat:      torch.Tensor,
        type_idx:  int,
        layer_idx: torch.Tensor,
    ) -> Tuple[torch.Tensor, Optional[torch.Tensor]]:
        key = self.weight_keys[type_idx]

        if key in self.matrix_keys:
            W = self.svd_heads[key](feat)     # [B, out, in]
            b = None
            if self.generate_biases:
                bias_key = key.replace("_weight", "_bias")
                if bias_key in self.bias_params:
                    # Index bias by layer
                    b = self.bias_params[bias_key][layer_idx]   # [B, b_dim]
            return W, b
        else:
            # Direct bias lookup
            b = self.bias_params[key][layer_idx]   # [B, b_dim]
            return b, None


