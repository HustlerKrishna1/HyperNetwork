class Hypernetwork(nn.Module):
    """
    H(layer_id, weight_type, z) → (W, b)

    Parameters
    ----------
    target_cfg   : config of the model whose weights are generated
    hypernet_cfg : config of this hypernetwork
    """

    def __init__(
        self,
        target_cfg:   TargetModelConfig,
        hypernet_cfg: HypernetworkConfig,
    ):
        super().__init__()
        self.target_cfg   = target_cfg
        self.hypernet_cfg = hypernet_cfg

        n_layers  = target_cfg.n_layers
        n_types   = hypernet_cfg.n_weight_types
        l_dim     = hypernet_cfg.layer_embed_dim
        t_dim     = hypernet_cfg.type_embed_dim
        lat_dim   = hypernet_cfg.latent_dim

        # ── Conditioning embeddings ──────────────────────────────────────────
        self.layer_embed = nn.Embedding(n_layers, l_dim)
        self.type_embed  = nn.Embedding(n_types,  t_dim)
        self.latent_proj = nn.Linear(lat_dim, lat_dim) if lat_dim > 0 else None

        cond_dim = l_dim + t_dim + lat_dim

        # ── MLP backbone ─────────────────────────────────────────────────────
        self.backbone = SiLUMLP(cond_dim, hypernet_cfg.hidden_dims, dropout=0.0)
        backbone_dim  = self.backbone.out_dim

        # ── Per-type output heads ────────────────────────────────────────────
        self.heads = PerTypeHeads(backbone_dim, target_cfg, hypernet_cfg)

        # ── Layer norm on backbone output ───────────────────────────────────
        self.out_norm = nn.LayerNorm(backbone_dim)

        # ── Learnable per-layer scale (helps stability) ──────────────────────
        self.layer_scale = nn.Parameter(
            torch.ones(n_layers, 1) * hypernet_cfg.init_scale
        )

        self._n_params = sum(p.numel() for p in self.parameters())

    # ── forward ──────────────────────────────────────────────────────────────

    def forward(
        self,
        layer_id:    torch.Tensor,           # [B] or scalar
        weight_type: torch.Tensor,           # [B] or scalar
        z:           Optional[torch.Tensor] = None,  # [B, latent_dim] or None
    ) -> Tuple[torch.Tensor, Optional[torch.Tensor]]:
        """
        Returns
        -------
        W : Tensor [B, out_dim, in_dim]  for matrix weights
            Tensor [B, dim]              for bias vectors
        b : Tensor [B, out_dim]  or  None
        """
        # Normalise scalar inputs to 1-D tensors
        layer_id    = _to_1d(layer_id)
        weight_type = _to_1d(weight_type)
        B = layer_id.size(0)

        device = next(self.parameters()).device
        layer_id    = layer_id.to(device)
        weight_type = weight_type.to(device)

        # Conditioning vector
        e_l = self.layer_embed(layer_id)    # [B, l_dim]
        e_t = self.type_embed(weight_type)  # [B, t_dim]

        if z is not None and self.latent_proj is not None:
            z_proj = self.latent_proj(z.to(device))   # [B, lat_dim]
            cond   = torch.cat([e_l, e_t, z_proj], dim=-1)
        else:
            # Pad latent with zeros if not provided
            lat_pad = torch.zeros(B, self.hypernet_cfg.latent_dim, device=device)
            cond    = torch.cat([e_l, e_t, lat_pad], dim=-1)

        # MLP backbone
        feat = self.backbone(cond)          # [B, backbone_dim]
        feat = self.out_norm(feat)

        # Per-layer scale (index by layer_id)
        scale = self.layer_scale[layer_id]  # [B, 1]
        feat  = feat * scale

        # Output heads  (pass layer_id for per-layer bias lookup)
        W, b = self.heads(feat, weight_type, layer_id)

        return W, b

    # ── convenience: generate ALL weights for one transformer block ──────────

    def generate_layer_weights(
        self,
        layer_idx: int,
        z: Optional[torch.Tensor] = None,
        device: Optional[torch.device] = None,
    ) -> Dict[str, torch.Tensor]:
        """
        Returns a dict  {weight_key: tensor}  for one transformer block.

        Shapes match target_cfg.weight_shapes exactly.
        """
        if device is None:
            device = next(self.parameters()).device

        weights: Dict[str, torch.Tensor] = {}
        for key, idx in WEIGHT_TYPE_INDEX.items():
            lid = torch.tensor([layer_idx], dtype=torch.long)
            tid = torch.tensor([idx],       dtype=torch.long)
            W, b = self(lid, tid, z)    # W : [1, out, in] or [1, out]

            # Squeeze batch dim
            w_sq = W.squeeze(0)
            weights[key] = w_sq

            # Some heads return both W and b in one shot (matrix weight + bias)
            # but PerTypeHeads returns b separately via heads_bias lookup.
            # b is returned only for matrix keys; bias keys return via W directly.
            if b is not None:
                bias_key = key.replace("_weight", "_bias")
                if bias_key in WEIGHT_TYPE_INDEX:
                    weights[bias_key] = b.squeeze(0)

        return weights

    # ── generate weights for all layers ──────────────────────────────────────

    def generate_all_weights(
        self,
        z: Optional[torch.Tensor] = None,
    ) -> List[Dict[str, torch.Tensor]]:
        """Returns list of weight dicts — one per transformer layer."""
        return [
            self.generate_layer_weights(i, z=z)
            for i in range(self.target_cfg.n_layers)
        ]

    # ── size utilities ────────────────────────────────────────────────────────

    @property
    def param_count(self) -> int:
        return self._n_params

    def size_mb(self, dtype_bytes: int = 4) -> float:
        return self._n_params * dtype_bytes / (1024 ** 2)

    def __repr__(self) -> str:  # type: ignore[override]
        return (
            f"Hypernetwork(\n"
            f"  strategy   = {self.hypernet_cfg.strategy}\n"
            f"  rank       = {self.hypernet_cfg.rank}\n"
            f"  backbone   = {self.hypernet_cfg.hidden_dims}\n"
            f"  params     = {self.param_count / 1e6:.3f} M\n"
            f"  size (FP32)= {self.size_mb():.2f} MB\n"
            f"  size (FP16)= {self.size_mb(2):.2f} MB\n"
            f")"
        )


# ---------------------------------------------------------------------------
# Shared-embedding variant (reduces param count further)
# ---------------------------------------------------------------------------

class SharedHypernetwork(Hypernetwork):
    """
    Variant where the MLP backbone parameters are **shared** across weight
    types.  Each type gets only a small adapter (16-dim) injected via FiLM
    (feature-wise linear modulation):

        feat = gamma(type) * backbone(cond) + beta(type)

    This can cut backbone params by ~50 % with minimal accuracy loss.
    """

    def __init__(self, target_cfg: TargetModelConfig, hypernet_cfg: HypernetworkConfig):
        super().__init__(target_cfg, hypernet_cfg)

        n_types  = hypernet_cfg.n_weight_types
        b_dim    = self.backbone.out_dim

        # FiLM modulation per weight type
        self.film_gamma = nn.Embedding(n_types, b_dim)
        self.film_beta  = nn.Embedding(n_types, b_dim)
        nn.init.ones_(self.film_gamma.weight)
        nn.init.zeros_(self.film_beta.weight)

    def forward(
        self,
        layer_id:    torch.Tensor,
        weight_type: torch.Tensor,
        z:           Optional[torch.Tensor] = None,
    ) -> Tuple[torch.Tensor, Optional[torch.Tensor]]:
        layer_id    = _to_1d(layer_id)
        weight_type = _to_1d(weight_type)
        B           = layer_id.size(0)
        device      = next(self.parameters()).device
        layer_id    = layer_id.to(device)
        weight_type = weight_type.to(device)

        e_l = self.layer_embed(layer_id)
        e_t = self.type_embed(weight_type)
        lat_pad = (z.to(device) if z is not None
                   else torch.zeros(B, self.hypernet_cfg.latent_dim, device=device))
        cond = torch.cat([e_l, e_t, lat_pad], dim=-1)

        feat  = self.backbone(cond)
        feat  = self.out_norm(feat)

        # FiLM conditioning
        gamma = self.film_gamma(weight_type)   # [B, b_dim]
        beta  = self.film_beta(weight_type)    # [B, b_dim]
        feat  = gamma * feat + beta

        scale = self.layer_scale[layer_id]
        feat  = feat * scale

        return self.heads(feat, weight_type, layer_id)


