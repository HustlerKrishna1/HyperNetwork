"""
hypernetwork.py  —  The core generator H(layer_id, weight_type, z) → W.

Architecture
============
                 ┌─────────────────────────────────────────┐
  layer_id ─────▶│  E_layer  [n_layers × layer_embed_dim]  │──┐
  weight_type ──▶│  E_type   [n_types  × type_embed_dim]   │  │ concat
  latent z  ────▶│  linear(latent_dim → latent_embed_dim)  │──┘
                 └─────────────────────────────────────────┘
                                    │
                              [cond_dim]
                                    │
                         ┌──────────┴──────────┐
                         │   MLP backbone      │
                         │  SiLU activations   │
                         │  LayerNorm between  │
                         └──────────┬──────────┘
                                    │
                             [hidden_last]
                                    │
                      ┌─────────────┼─────────────┐
                      │             │             │
               ┌──────▼──┐   ┌──────▼──┐   ┌──────▼──┐
               │ Head_A   │   │ Head_B   │   │ Head_b  │
               │ →[out,r] │   │ →[r,in]  │   │ →[out]  │
               └──────────┘   └──────────┘   └─────────┘
                      │             │
                      └──── A @ B ──┘
                           W = A @ B   (scaled)

Generated weight:  W = A @ B   ∈ ℝ^{out × in}
Generated bias:    b           ∈ ℝ^{out}

Tensor shapes (default config, hidden_dim=256, ffn_dim=1024, rank=32):
  layer_id      : scalar int  or  LongTensor [B]
  weight_type   : scalar int  or  LongTensor [B]
  z             : None  or  Tensor [B, latent_dim]

  conditioning  : [B, cond_dim]      = [B, 64+64+32] = [B, 160]
  MLP output    : [B, 512]
  Head_A output : [B, out × rank]    → reshape → [B, out, rank]
  Head_B output : [B, rank × in]     → reshape → [B, rank, in]
  W             : [B, out, in]       A @ B  via einsum / bmm
  b             : [B, out]
"""

