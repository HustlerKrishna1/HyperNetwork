class SiLUMLP(nn.Module):
    """
    Feed-forward network with SiLU activations and pre-norm.

    input_dim → hidden_dims[0] → … → hidden_dims[-1]
    """
    def __init__(self, input_dim: int, hidden_dims: List[int], dropout: float = 0.0):
        super().__init__()
        layers: List[nn.Module] = []
        prev = input_dim
        for h in hidden_dims:
            layers.append(nn.Linear(prev, h))
            layers.append(nn.SiLU())
            if dropout > 0:
                layers.append(nn.Dropout(dropout))
            prev = h
        self.net = nn.Sequential(*layers)
        self.out_dim = prev
        self._init_weights()

    def _init_weights(self):
        for m in self.modules():
            if isinstance(m, nn.Linear):
                nn.init.kaiming_uniform_(m.weight, a=math.sqrt(5))
                if m.bias is not None:
                    fan_in, _ = nn.init._calculate_fan_in_and_fan_out(m.weight)
                    bound = 1 / math.sqrt(fan_in)
                    nn.init.uniform_(m.bias, -bound, bound)

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        return self.net(x)


class SVDWeightHead(nn.Module):
    """
    SVD-based weight generator:  W = U @ diag(s) @ V^T

    U ∈ R^{out × rank} and V ∈ R^{in × rank} are stored as learnable parameters.
    The hypernetwork generates only the rank singular values  s ∈ R^{rank}.

    This is dramatically more parameter-efficient than generating A and B
    directly via linear heads:

    Direct (WeightHead):  backbone_dim × (out*rank + rank*in)
                        = 512 × (256×32 + 32×256) = 8.4 M params  ❌

    SVD (this class):     backbone_dim × rank  +  out*rank  +  in*rank
                        = 512×32 + 256×32 + 256×32 = 32 K params  ✅

    Tensor shapes (out=256, in=256, rank=32, B=4):
        feat  : [B, 512]
        s     : [B, 32]         (generated by the tiny s-head)
        U     : [256, 32]       (stored parameter)
        V     : [256, 32]       (stored parameter)
        W     : [B, 256, 256]   via einsum('or,br,ir->boi', U, s, V)
    """

    def __init__(self, backbone_dim: int, out_dim: int, in_dim: int, rank: int,
                 scale: float = 0.02):
        super().__init__()
        self.out_dim  = out_dim
        self.in_dim   = in_dim
        self.rank     = rank

        # Stored basis matrices — learned but NOT generated per-forward-pass
        self.U = nn.Parameter(torch.empty(out_dim, rank))   # [out, r]
        self.V = nn.Parameter(torch.empty(in_dim,  rank))   # [in,  r]

        # Tiny s-head: backbone_dim → rank  (ONLY this is computed on-the-fly)
        self.s_head = nn.Linear(backbone_dim, rank)

        # Orthogonal init for U, V; small init for s_head
        nn.init.orthogonal_(self.U)
        nn.init.orthogonal_(self.V)
        nn.init.normal_(self.s_head.weight, std=scale / math.sqrt(backbone_dim))
        nn.init.zeros_(self.s_head.bias)

    def forward(self, feat: torch.Tensor) -> torch.Tensor:
        """
        feat : [B, backbone_dim]
        Returns W : [B, out_dim, in_dim]
        """
        s = self.s_head(feat)                                  # [B, rank]
        # W[b, i, j] = Σ_r  U[i,r] * s[b,r] * V[j,r]
        W = torch.einsum("or, br, ir -> boi", self.U, s, self.V)
        return W                                               # [B, out, in]

    @property
    def stored_params(self) -> int:
        return self.U.numel() + self.V.numel()

    @property
    def generated_params(self) -> int:
        return self.rank   # only s is generated


