# Utilities
# ---------------------------------------------------------------------------

def _to_1d(t: torch.Tensor) -> torch.Tensor:
    """Ensure tensor is at least 1-D."""
    if t.dim() == 0:
        return t.unsqueeze(0)
    return t


def build_hypernetwork(
    target_cfg:   TargetModelConfig,
    hypernet_cfg: HypernetworkConfig,
) -> Hypernetwork:
    """Factory that picks the right variant based on config."""
    if hypernet_cfg.share_weights:
        return SharedHypernetwork(target_cfg, hypernet_cfg)
    return Hypernetwork(target_cfg, hypernet_cfg)


def count_parameters(model: nn.Module) -> Dict[str, int]:
    """Returns total, trainable, and non-trainable param counts."""
    total     = sum(p.numel() for p in model.parameters())
    trainable = sum(p.numel() for p in model.parameters() if p.requires_grad)
    return {
        "total":     total,
        "trainable": trainable,
        "frozen":    total - trainable,
    }
